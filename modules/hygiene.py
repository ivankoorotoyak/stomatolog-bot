def get_hygiene_advice(text):
    """Основная функция: принимает текст, возвращает совет"""
    text = text.lower()
    result = []
    
    for problem, data in HYGIENE_DATA.items():
        if problem in text:
            result.append(f"🔹 *{problem.capitalize()}*")
            for category, advice in data.items():
                result.append(f"  • *{category.capitalize()}*: {advice}")
            result.append("")
    
    for category in GENERAL_TIPS.keys():
        if category in text:
            result.append(f"🔸 *{category.capitalize()}*: {GENERAL_TIPS[category]}")
    
    if not result:
        return None
    
    result.append(SHOP_TIPS)
    return "\n".join(result)
